    private void btnfirstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfirstActionPerformed
            
         try{
        query = "select flightname,destiname,departureplace,departuretime,arrivaldatetime,price from flight";
              
              rs=st.executeQuery(query);
              
              while(rs.next()){
                  if(rs.isFirst()){
                      txtflightname.setText(rs.getString("flightname"));
                 txtdestinationplace.setText(rs.getString("destiname"));
                 txtdepartureplace.setText(rs.getString("departureplace"));
                 txtdeparturedate.setText(rs.getString("departuretime"));
                 txtarrivaldate.setText(rs.getString("arrivaldatetime"));
                 txtprice.setText(rs.getString("price"));
                  }
              }
              
        }
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null,"there is no record at this position");
            txtflightname.setText("");
            txtflightname.requestFocus();
        }
    }//GEN-LAST:event_btnfirstActionPerformed

