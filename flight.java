    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        
        
        if(txtflightname.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter the flight name");
            txtflightname.setText("");
            txtflightname.requestFocus();
            
        }
          if(txtdestinationplace.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter the destination place");
            txtdestinationplace.setText("");
            txtdestinationplace.requestFocus();
            
        }
           if(txtdepartureplace.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter the destination place");
            txtdepartureplace.setText("");
            txtdepartureplace.requestFocus();
            
        }
            if(txtdeparturedate.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter the derpature date");
            txtdeparturedate.setText("");
            txtdeparturedate.requestFocus();
            
        }
             if(txtarrivaldate.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter the arrival date");
            txtarrivaldate.setText("");
            txtarrivaldate.requestFocus();
            
        }
              if(txtprice.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter the price of the flight");
            txtprice.setText("");
            txtprice.requestFocus();
            
        }
              String s1,s2,s3,s4,s5,s6;
              s1=txtflightname.getText();
              s2=txtdestinationplace.getText();
              s3=txtdepartureplace.getText();
              s4=txtdeparturedate.getText();
              s5=txtarrivaldate.getText();
              s6=txtprice.getText();
              
              try{
              query ="select licencenumber from driver";
              rs = st.executeQuery(query);
              while(rs.next()){
                  if(s6.equals(rs.getString("flightname"))){
                      JOptionPane.showMessageDialog(null, "the flight name already exists \nplease enter another flight name");
                      txtflightname.setText("");
                      txtflightname.requestFocus();
                  }
              }
              query = "INSERT INTO flight( flightname,destiname,departureplace,departuretime,arrivaldatetime,price)values('"+s1+"','"+s2+"','"+s3+"','"+s4+"','"+s5+"','"+s6+"')";
              JOptionPane.showConfirmDialog(null, "DO YOU REALLY WANT TO SAVE THE ABOVE INFORMATION");
              st.executeUpdate(query);
              txtflightname.setText("");
              txtdestinationplace.setText("");
              txtdepartureplace.setText("");
              txtdeparturedate.setText("");
              txtarrivaldate.setText("");
              txtprice.setText("");
              }
              catch(SQLException ex){
                  ex.printStackTrace();
              }
    }//GEN-LAST:event_btnsaveActionPerformed

